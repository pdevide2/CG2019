CREATE TRIGGER TGR_DEL_CG_ENTRADA_CHIP_ITEM ON CG_ENTRADA_CHIP_ITEM FOR DELETE
AS
BEGIN

	DECLARE @ID_CHIP INT
	
	SELECT @ID_CHIP = deleted.ID_CHIP
	FROM deleted

	UPDATE CG_CHIP 
	SET ESTOQUE_MATRIZ = 0, ESTOQUE_TRANSITO=0, ESTOQUE_LOJA = 0, ID_LOCAL_ESTOQUE=0
	WHERE ID_CHIP = @ID_CHIP


END

