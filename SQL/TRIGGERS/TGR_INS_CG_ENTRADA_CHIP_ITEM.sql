CREATE TRIGGER TGR_INS_CG_ENTRADA_CHIP_ITEM ON CG_ENTRADA_CHIP_ITEM FOR INSERT
AS
BEGIN

	DECLARE @ID_CHIP INT
	
	SELECT @ID_CHIP = inserted.ID_CHIP
	FROM inserted

	UPDATE CG_CHIP 
	SET ESTOQUE_MATRIZ = 1, ESTOQUE_TRANSITO=0, ESTOQUE_LOJA = 0, ID_LOCAL_ESTOQUE=1
	WHERE ID_CHIP = @ID_CHIP


END

