CREATE TABLE ROMANEIO_ESTOQUE (
ROMANEIO INT NOT NULL IDENTITY PRIMARY KEY,
MODULO VARCHAR(50) NULL, /*NOME DA TELA - EXEMPLO: ENTRADA DE CHIP */
TIPO_PRODUTO CHAR(1) NOT NULL, /*"C" = CHIP, "E" = EQUIPAMENTO */
TIPO_OP CHAR(1) NULL, /*I = INCLUSÃO, A = ALTERAÇÃO, E = EXCLUSÃO */
DATAMOV DATETIME NOT NULL DEFAULT (GETDATE()),
ID_USUARIO INT NOT NULL )
GO

CREATE TABLE ROMANEIO_ESTOQUE_ITEM (
ROMANEIO INT NOT NULL,
UNIQUE_KEY UNIQUEIDENTIFIER NOT NULL,
ID_PRODUTO INT NOT NULL,
ID_TRANSACAO CHAR(3) NOT NULL,
TRANSITO BIT NOT NULL,
ID_LOCAL_DE INT NOT NULL,
ID_LOCAL_PARA INT NOT NULL,
DATAMOV DATETIME NOT NULL DEFAULT (GETDATE()),
ENTRADA_SAIDA CHAR(1) NOT NULL,
QUANTIDADE INT NOT NULL,
OBSERVACAO VARCHAR(200) NULL)
GO

